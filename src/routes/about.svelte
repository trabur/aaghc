	<title>How to play AAGHC</title>
</svelte:head>

<h1>The rules of this game.</h1>
<ul>
	<li>H: start game</li>
	<li>AA is M</li>
	<li>RGB ... red, green, blue</li>
	<li>R45 --> rock, paper, scissors</li>
	<li>VISA // down = up</li>
	<li>HELLO: finish game</li>
</ul>

<h1>The parts of this game.</h1>
<ul>
	<li>b.d.p.q: pawn letters</li>
	<li>3: board</li>
	<li>2: king</li>
	<li>8: qween</li>
	<li>X: bishop</li>
	<li>L: knight</li>
	<li>+: castle</li>
</ul>

<h1>The stages of this game.</h1>
<ul>
	<li>/: check</li>
	<li>(: win</li>
	<li>): lose</li>
	<li>?: draw</li>
	<li>1: defend</li>
	<li>0: attack</li>
	<li>esc: reset clock</li>
	<li>space: shift clock</li>
	<li>F: message</li>
</ul>